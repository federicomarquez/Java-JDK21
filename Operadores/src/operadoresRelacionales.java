public class operadoresRelacionales {
    public static void main(String[] args) {
        //operadores relacionales
        int a = 3, b = 2;
        boolean resultado = a < b;
        System.out.println("a < b = " + resultado);
        resultado = a <= b;
        System.out.println("a <= b = " + resultado);
        resultado = a > b;
        System.out.println("a > b = " + resultado);
        resultado = a >= b;
        System.out.println("a >= b = " + resultado);
        //igualdad ==
        resultado = a == b;
        System.out.println("a == b = " + resultado);
        //diferente !=
        resultado = a != b;
        System.out.println("a != b = " + resultado);
    }
}
