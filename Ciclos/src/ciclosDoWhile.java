public class ciclosDoWhile {
    public static void main(String[] args) {
        //imprimir del 1 al 5
        //ciclos while
        var contador = 1;
        final var VALOR_MAXIMO = 5;
        do {
            System.out.println("contador = " + contador++);
        }while(contador <= VALOR_MAXIMO);




    }
}
