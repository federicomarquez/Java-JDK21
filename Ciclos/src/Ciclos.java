public class Ciclos {
    public static void main(String[] args) {
        //imprimir del 1 al 5
        //ciclos while
        var contador = 1;
        final var VALOR_MAXIMO = 100;
        while(contador <= VALOR_MAXIMO){
            System.out.println("contador = " + contador);
            contador++;
        }

    }
}
