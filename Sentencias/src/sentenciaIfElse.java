public class sentenciaIfElse {
    public static void main(String[] args) {
        //sentencia decision if/else
        var llueve = false;
        var nublado = false;
        if (llueve) {
            System.out.println("Hoy llueve");
        } else if (nublado) {
            System.out.println("Hoy esta nublado");
        } else {
            System.out.println("Hoy no llueve ni esta nublado");
        }
    }
}
